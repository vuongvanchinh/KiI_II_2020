	class DocumentValidateListener implements DocumentListener {
		public void changedUpdate(DocumentEvent e) {
				changed();
		}
		public void removeUpdate(DocumentEvent e) {
				changed();
		}
		public void insertUpdate(DocumentEvent e) {
				changed();
		}

		public void changed() {
			if (crudWordValidate() && crudMeanValidate() && crudDscValidate()){
				crudButton.setEnabled(true);
				return;
			}
			crudButton.setEnabled(false);
		}
	}

	public boolean crudWordValidate() {
		if (crudWord.getText().equals("")) {
			System.out.println("word invalid");
		}
		return !crudWord.getText().equals("");
	}

	public boolean crudMeanValidate() {
		if (crudMean.getText().equals("")) {
			System.out.println(crudMean.getText() + "mean invalid");
		}
		return !crudMean.getText().equals("");
	}

	public boolean crudDscValidate() {
		System.out.println("number of crudeDsc: " + crudDsc.getText().split("\n").length);
		return crudDsc.getText().split("\n").length >= 4;

	}

	public boolean crudProValidate() {
        //crudPro.setText(standardString(crudPro.getText()));
		if (crudPro.getText().equals("")) {
			System.out.println("pronunciation invalid");
		}
		return !crudPro.getText().equals("");
	}

	public boolean crudPartOfSpeechValidate() {
		if (crudPartOfSpeech.getText().equals("")) {
			System.out.println("mean invalid");
		}
		return !crudPartOfSpeech.getText().equals("");
        }
        
        public static String standardString(String s) {
			if (s.equals("")) {return s;}
			s = s.trim().replaceAll("\\s+", " ");
			if (s.equals("")) {return s;}
			return s;
        }
