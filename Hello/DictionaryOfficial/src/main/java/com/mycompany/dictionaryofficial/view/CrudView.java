    public void initFormEdit(Word word) {
        this.crudTitle.setText("Edit word");
        this.crudButton.setText("Update");
        this.crudWord.setText(word.getWordTarget());
        this.crudMean.setText(word.getWordExplain());
        this.crudPro.setText(word.getPronunciation());
        this.crudDsc.setText(word.getDescription().replace("\t", "\n"));
        this.crudPartOfSpeech.setText(word.getPartOfSpeech());
        this.crudSynonym.setText(word.getSynonym());
        this.crudWord.requestFocus();
        this.crudButton.setActionCommand("edit");
    }

